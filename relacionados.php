<?php


class relacionados
{
private $idSe;
private $idRel;
private $tipo;

    /**
     * @return mixed
     */
    public function getIdSe()
    {
        return $this->idSe;
    }

    /**
     * @return mixed
     */
    public function getIdRel()
    {
        return $this->idRel;
    }

    /**
     * @return mixed
     */
    public function getTipo()
    {
        return $this->tipo;
    }


}